var pref_list = Object.keys(pref_city_data);

window.onload = function(){ 
    // ページ読み込み時の処理
    // 都道府県のプルダウンメニューの作成
    for (pref_name of pref_list) {
        var op = document.createElement("option");
        op.value = pref_name;
        op.name = pref_name;
        op.text = pref_name;
        document.getElementById("prefectures").appendChild(op);
    }
    change();
}

function change() {
    var select_pref_name, city_list;

    //選択される都道府県が変更された際に市町村データをリセット
    if(document.getElementById("city").value != null) {
        var element = document.getElementById("city");
        while (element.firstChild)
        {
            element.removeChild(element.firstChild);
        }
    }
    
    select_pref_name = document.getElementById("prefectures").value;

    var city_op = document.createElement("option");
    var city_list = Object.keys(pref_city_data[select_pref_name]);
    
    //市町村プルダウンメニュー作成
    for (city_name of city_list) {
        var city_op = document.createElement("option");
        city_op.value = city_name;
        //city_op.value = pref_city_data[select_pref_name][city_name];
        city_op.name = city_name;
        city_op.text = city_name;
        document.getElementById("city").appendChild(city_op);
    }
}

